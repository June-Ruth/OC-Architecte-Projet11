package com.medhead.selenium.object;

public class Hospital {

    public Hospital() {

    }

    private String name;

    private String address1;

    private String address2;

    private String address3;

    private String city;

    private String county;

    private String postcode;

    public String getName() {
        return name;
    }

    public void setName(String nameP) {
        name = nameP;
    }

    public String getAddress1() {
        return address1;
    }

    public void setAddress1(String address1P) {
        address1 = address1P;
    }

    public String getAddress2() {
        return address2;
    }

    public void setAddress2(String address2P) {
        address2 = address2P;
    }

    public String getAddress3() {
        return address3;
    }

    public void setAddress3(String address3P) {
        address3 = address3P;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String cityP) {
        city = cityP;
    }

    public String getCounty() {
        return county;
    }

    public void setCounty(String countyP) {
        county = countyP;
    }

    public String getPostcode() {
        return postcode;
    }

    public void setPostcode(String postcodeP) {
        postcode = postcodeP;
    }
}
